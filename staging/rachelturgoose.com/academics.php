		<div class="philosophies">
			<h1>Teaching Philosophy</h1>
			<p class="section-intro-paragraph">
				Good instructors are able to leverage their knowledge as <span>experts and educators</span> with a <span>focused, consistent strategy</span> for reaching their students.
				<br><br>
				<span>Great</span> instructors go a step further, reaching out to their students as <span>fellow learners</span> at the beginning of a long journey of discovery, both in their discipline and in life.
				<br><br>
				Rachel's words on her teaching philosophy speak directly to these values:
			</p>
			<p class="quoted">There are few things more personal than music. As music educators, it is our job not only to inspire creativity through this art, but to impart musical knowledge and encourage expression and self-discovery. This is no small task. It is my belief that the student benefits the most from a teacher that never stops learning herself, and that elements such as environment, positive reinforcement, and appropriate feedback are crucial to the success of each child. 
			<br><br>
			Experience has taught me that the best teachers are those who never stop working to improve their craft, and those who constantly seek new information and find different ways to incorporate their own musical and personal growth into their classrooms. By improving teaching tactics, training in new technology, learning new instruments and improving upon studied ones, etc., teachers are furthering their own education and can therefore enrich the education they are giving to their students. Music education is a complex process in which students learn far more than simple facts and figures. Through music, a child learns that dedication to practice, self-discipline, and teamwork can turn a great deal of individual voices into a single ensemble that is capable of far more than any student could achieve on their own. Participation in such ensembles also allows students to gain lessons in humility and responsibility, all while learning to make beautiful sounds of which they can be proud.
			<br><br>
			Creating a safe, enthusiastic environment is crucial to instilling these important life lessons. Because music is so personal, students can be left feeling vulnerable, and it is important to constantly reinforce the idea that questions are welcome and mistakes are merely opportunities to learn. Positive reinforcement, encouragement, and corrective feedback go much farther than negative, and the development of one’s character is as important as the development of one’s musical skill (I believe my teacher put it best when he said “Being a good person is just as important as being a good musician.”) I try my best to “practice what I preach” on that issue in particular, and strive to set an example for my students so that they leave my class not only as better musicians, but as better people.
			<br><br>
			I believe that teaching itself is a skill and an art. Musicians spend a lifetime developing their instruments, and teaching is no different. I like to think of teachers as jazz improvisers. The fundamentals of music (scales, chords, etc.) are like the fundamentals of teaching (classroom management, teaching techniques, etc.) After years of musical study and countless hours of practice, a great improviser learns how to build a beautiful melody from that musical foundation. Teaching requires that same level of dedication and practice to successfully mold the human mind in all its vast and varying forms. Improvisation is key to teaching a classroom of individual minds, as one method of learning for few students may not work for others, and it is only through commitment to perfecting this craft that this art form will truly take shape.
			</p>
